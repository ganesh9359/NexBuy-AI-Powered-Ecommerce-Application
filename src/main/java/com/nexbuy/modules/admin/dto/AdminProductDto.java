package com.nexbuy.modules.admin.dto;

import java.time.LocalDateTime;
import java.util.ArrayList;
import java.util.List;

public class AdminProductDto {
    private Long id;
    private String title;
    private String slug;
    private String description;
    private String coverImage;
    private String status;
    private String categoryName;
    private String brandName;
    private String sku;
    private String variantName;
    private int priceCents;
    private Integer compareAtCents;
    private int stockQty;
    private LocalDateTime createdAt;
    private List<String> tags = new ArrayList<>();
    private List<AdminProductMediaDto> media = new ArrayList<>();

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSlug() {
        return slug;
    }

    public void setSlug(String slug) {
        this.slug = slug;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCoverImage() {
        return coverImage;
    }

    public void setCoverImage(String coverImage) {
        this.coverImage = coverImage;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public String getSku() {
        return sku;
    }

    public void setSku(String sku) {
        this.sku = sku;
    }

    public String getVariantName() {
        return variantName;
    }

    public void setVariantName(String variantName) {
        this.variantName = variantName;
    }

    public int getPriceCents() {
        return priceCents;
    }

    public void setPriceCents(int priceCents) {
        this.priceCents = priceCents;
    }

    public Integer getCompareAtCents() {
        return compareAtCents;
    }

    public void setCompareAtCents(Integer compareAtCents) {
        this.compareAtCents = compareAtCents;
    }

    public int getStockQty() {
        return stockQty;
    }

    public void setStockQty(int stockQty) {
        this.stockQty = stockQty;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public List<String> getTags() {
        return tags;
    }

    public void setTags(List<String> tags) {
        this.tags = tags;
    }

    public List<AdminProductMediaDto> getMedia() {
        return media;
    }

    public void setMedia(List<AdminProductMediaDto> media) {
        this.media = media;
    }
}
